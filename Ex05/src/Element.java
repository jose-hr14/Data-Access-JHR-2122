public abstract class Element
{
    protected float coordX;
    protected float coordY;

    public void setCoordX(float coordX)
    {
        this.coordX = coordX;
    }

    public float getCoordX() {
        return coordX;
    }

    public void getCoordY(float coordY)
    {
        this.coordY = coordY;
    }

    public void setCoordY(float coordY) {
        this.coordY = coordY;
    }
}
